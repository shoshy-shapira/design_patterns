package Verisoft.ChocolateBarFactory;

/**
 * Abstract class representing a generic chocolate bar.
 * Defines methods for producing and packaging the chocolate bar.
 */
public abstract class ChocolateBar {
    /**
     * Produces the chocolate bar according to the recipe.
     */
    public abstract void produce();

    /**
     * Packages the chocolate bar.
     */
    public abstract void packageBar();

}
