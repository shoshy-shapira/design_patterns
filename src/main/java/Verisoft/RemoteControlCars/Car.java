package Verisoft.RemoteControlCars;

public interface Car {
    /**
     * Moves the car forward.
     */
    void driveForward();

    /**
     * Moves the car backward.
     */
    void driveBackward();

    /**
     * Turns the car left.
     */
    void turnLeft();

    /**
     * Turns the car right.
     */
    void turnRight();
    }
